using System.Collections.Generic;
using System.Linq;
using System.Text;
using IntegrityTables.SourceGeneration.Model;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace IntegrityTables.SourceGeneration;

public class EntityExtensionsGenerator
{
    public static void GenerateCode(SourceProductionContext context, DatabaseModel model)
    {
        var sb = new StringBuilder();
        sb.AppendLine($@"// <auto-generated/> //  {DatabaseSourceGenerator.GenerationStamp()}
{(string.IsNullOrEmpty(model.NameSpace)?"":$"namespace {model.NameSpace}.EntityExtensions;")}        

public static class EntityExtensions {{

}}
");
        context.AddSource($"{model.FileName("EntityExtensions", model.DatabaseSymbol.Name)}.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));


    }
}